class Solution
{
	public:
		string is_palindrome(int n)
		{
		      string s=to_string(n);
              string s1=to_string(n);
              reverse(s.begin(),s.end());
              if(s1==s) cout<<"Yes";
              else cout<<"No";
		}
};

//{ Driver Code Starts.
int main(){
    int T;
    cin >> T;
    while(T--)
    {
    	int n;
    	cin >> n;
    	Solution ob;
    	string ans = ob.is_palindrome(n);
    	cout << ans <<"\n";
    }
	return 0;
}

